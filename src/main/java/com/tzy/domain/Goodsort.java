package com.tzy.domain;

public class Goodsort {
private Integer id;
private String name;
public Integer getId() {
	return id;
}
public void setId(Integer id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public Goodsort() {
	super();
}
public Goodsort(Integer id, String name) {
	super();
	this.id = id;
	this.name = name;
}
	
}
